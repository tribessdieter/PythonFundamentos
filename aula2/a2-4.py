# CRIAÇÃO DE LISTAS NUMERICA
range(5)  # cria um conjunto a varialvel numerico 0 a 5 

# atribui cada valor do conjunto a varialvel numero
# ou seja ,executa o codigo repetidamente por 5 vezes(de 0 a 4)
for numero in range(5):
    print(numero)

#comecando em 1 indo ate 5
for numero in range(1,5):
    print(numero)

# comecando em 1 ate 9 e pulando de 2 em 2
for numero in range(1,9,2):
    print(numero)

